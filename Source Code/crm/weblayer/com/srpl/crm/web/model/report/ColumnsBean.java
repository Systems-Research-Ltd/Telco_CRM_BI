package com.srpl.crm.web.model.report;

import java.io.Serializable;

public class ColumnsBean implements  Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String colDB;
	private String col;
	
	public ColumnsBean(String colDB, String col){
		this.colDB = colDB;
		this.col = col;
	}
	
	public String getColDB() {
		return colDB;
	}
	public void setColdb(String colDB) {
		this.colDB = colDB;
	}
	public String getCol() {
		return col;
	}
	public void setCol(String col) {
		this.col = col;
	}

}
