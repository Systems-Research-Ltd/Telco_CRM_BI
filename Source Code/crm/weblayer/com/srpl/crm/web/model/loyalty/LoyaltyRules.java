package com.srpl.crm.web.model.loyalty;

import java.io.Serializable;

public class LoyaltyRules implements Serializable {
	
	private int tempRuleId;
	private Long loyaltyRuleId;
	private String loyaltyaRule;
	private String loyaltyRuleValue;
	private String loyaltyRuleCondition;
	
	
	public int getTempRuleId() {
		return tempRuleId;
	}

	public void setTempRuleId(int tempRuleId) {
		this.tempRuleId = tempRuleId;
	}

	public Long getLoyaltyRuleId() {
		return loyaltyRuleId;
	}

	public void setLoyaltyRuleId(Long loyaltyRuleId) {
		this.loyaltyRuleId = loyaltyRuleId;
	}

	
	public String getLoyaltyaRule() {
		return loyaltyaRule;
	}

	public void setLoyaltyaRule(String loyaltyaRule) {
		this.loyaltyaRule = loyaltyaRule;
	}

	public String getLoyaltyRuleValue() {
		return loyaltyRuleValue;
	}

	public void setLoyaltyRuleValue(String loyaltyRuleValue) {
		this.loyaltyRuleValue = loyaltyRuleValue;
	}

	public String getLoyaltyRuleCondition() {
		return loyaltyRuleCondition;
	}

	public void setLoyaltyRuleCondition(String loyaltyRuleCondition) {
		this.loyaltyRuleCondition = loyaltyRuleCondition;
	}


}
